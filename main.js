const select = document.getElementById("chara");
const result = document.getElementById("rslt");
const image = document.getElementById("chara-img");

let typingTimeout = null;
let isTyping = false;
let fullText = "";
let isFirstLoad = true;

function typeWriter(element, text, baseSpeed = 80) {
  let i = 0;
  element.textContent = "";
  fullText = text;
  isTyping = true;

  if (typingTimeout) clearTimeout(typingTimeout);

  function type() {
    if (i >= text.length) {
      isTyping = false;
      return;
    }

    element.textContent += text.charAt(i);

    let delay = baseSpeed;
    const char = text.charAt(i);

    if (char === "、") delay = 150;
    if (char === "。") delay = 300;
    if (char === "\n") delay = 200;

    i++;
    typingTimeout = setTimeout(type, delay);
  }

  type();
}

function updateCharacter(selected) {
  const data = charaData[selected];
  if (!data) return;

  if (!isFirstLoad) {
    image.classList.add("fade-out");

    setTimeout(() => {
      image.src = `images/${selected}.png`;
      image.classList.remove("fade-out");
    }, 400);
  } else {
    image.src = `images/${selected}.png`;
    isFirstLoad = false;
  }

  result.innerHTML = `
    <h2>${data.name}</h2>
    <ul class="profile">
      <li>年齢：${data.age}</li>
      <li>誕生日：${data.birthday}</li>
      <li>血液型：${data.blood}</li>
      <li>身長：${data.height}</li>
      <li>作品：${data.work}</li>
      <li>マーク：${data.mark}</li>
    </ul>
    <p class="description" id="desc"></p>
  `;

  const descElement = document.getElementById("desc");

  typeWriter(descElement, data.description, 80);

  descElement.addEventListener("click", () => {
    if (isTyping) {
      clearTimeout(typingTimeout);
      descElement.textContent = fullText;
      isTyping = false;
    }
  });
}

select.addEventListener("change", () => {
  updateCharacter(select.value);
});

window.addEventListener("load", () => {
  const loader = document.getElementById("loader");

  setTimeout(() => {
    loader.classList.add("fade-out");
    updateCharacter("1st");
  }, 1500);
});
