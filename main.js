function typeWriter(element, text, baseSpeed = 80) {
  let i = 0;
  element.textContent = "";
  fullText = text;
  isTyping = true;

  if (typingTimeout) clearTimeout(typingTimeout);

  function type() {
    if (i >= text.length) {
      isTyping = false;
      return;
    }

    element.textContent += text.charAt(i);

    let delay = baseSpeed;
    const char = text.charAt(i);

    if (char === "ã€") delay = 150;
    if (char === "ã€‚") delay = 300;
    if (char === "\n") delay = 200;

    i++;
    typingTimeout = setTimeout(type, delay);
  }

  type();
}

